package Aintroducao;

public class Aula01TiposPrimitivos {

    public static void main(String[] args) {

        // Valores inteiros - diferença é apenas o tamanho.

        byte idadeByte = 127; // Aceita numeros entre -128 ate 127 positivo.
        short idadeShort = 12345; // Aceita apenas 5 numeros.

        int idade = 20;
        long idadeLong = 30;

        // Valores moeda diferença é apenas o tamanho.
        double real = 2.000D;
        float realFloat = 3.000F;


        boolean verdadeiro = true;
        boolean falso = false;

        char caractere = 'A'; // Aceita apenas um caractere.

        String nome = "joão";

    }

}
